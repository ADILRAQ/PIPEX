#include "../header.h"

int	ft_putstr(char *s)
{
	int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	i = 0;
	while (*s)
	{
		write(1, s++, 1);
		i++;
	}
	return (i);
}
